/**
 * op_add - adds 2 ints.
 * @a: first operand.
 * @b: second operand.
 * Return: addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts 2 ints.
 * @a: first operand.
 * @b: second operand.
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies 2 ints.
 * @a: first operand.
 * @b: second operand.
 * Return: product.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides 2 ints.
 * @a: first operand.
 * @b: second operand.
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mods 2 ints.
 * @a: first operand.
 * @b: second operand.
 * Return: modulo.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
